#include <iostream>
using namespace std;

int main() {
    int n,a,i,y=0,z=0;
    cin >> n;
    for (i=0;i<n;i++)
    {
        cin >>a;
        if (a%2==0)
        y=y+1;
        else
        z=z+1;
    }
    if (y>z)
    {
        cout << "Ready for Battle";
    }
    else
    {
        cout << "Not Ready";
    }
	return 0;
}
